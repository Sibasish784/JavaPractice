public class airth_oper {
    public static void main(String[] args) {
        System.out.print("The sum of these number is :- ");

        int num1 = 7;
        int num2 = 8;
        int num3 = 4;
        int sum = num1 + num2 + num3;
        int sub = num1 - num2 - num3;
        int mul = num1 * num2 * num3;
        int div = num1 / num2 / num3;
        System.out.println(sum);
        System.out.print("The sub of these number is :- ");
        System.out.println(sub);
        System.out.print("The mul of these number is :- ");
        System.out.println(mul);
        System.out.print("The div of these number is :- ");
        System.out.println(div);
    }
}
